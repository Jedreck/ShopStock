public class StoreApp
{
    private String id;
    private String name;
    private String price;
    private String stock;
    public String getId()
    {
        return id;
    }
    public void setId(String id)
    {
        this.id=id;
    }
    public String getName()
    {
        return name;
    }
    public void setName()
    {
        this.name=name;
    }
    public String getPrice()
    {
        return price;
    }
    public void setPrice()
    {
        this.price=price;
    }
    public String getStock()
    {
        return stock;
    }
    public void setStock()
    {
        this.stock=stock;
    }
}
